char current = 0;
void putsym(char sym)
{
	short voltage;
	current += 2 + (sym & 16);
	if (current > 17) current -= 18;
	// 12 bit dac = 2v, 1kHz = 10 bits
	// step = 16KHz/1024 => 16, 4bits
	voltage = (current << 4) + 1024;
	//DAC = voltage;

	lpdelay(64);
}

void domino_tx(char txchar)
{
	short tx = txchar & 127;
	putsym(varicode[tx][0]);
	if (varicode[tx][1]) putsym(varicode[tx][1]);
	if (varicode[tx][2]) putsym(varicode[tx][2]);
}
